int isNegative(int x) {
  return ((x&(1<<31))>>31)&1;
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
  int sign=1<<31;
  int _x=(~x+1);
  int maskX=(~(x&sign))>>31;
  int mask_X=~maskX;
  return (x&maskX) | (_x&mask_X);
}
/* 
 * float_abs - Return bit-level equivalent of absolute value of f for
 *   floating point argument f.
 *   Both the argument and result are passed as int int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/int operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
int float_abs(int uf) {
  int maskEXP=0xFF<<23;
  int exp=uf&maskEXP;
  int frac=uf&((1<<23)-1);

  if(exp==maskEXP && frac!=0) return uf;

  return exp|frac;
}
/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as int int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/int operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(int uf) {
  int sign=(uf>>31)&1;
  int exp=(uf>>23)&0xFF;
  unsigned frac=uf&((1<<23)-1);

  int Ox80000000 = 1<<31;

  if (exp==0xFF) return Ox80000000;
  exp-=0x7F;

  if (exp<0) return 0;
  if (exp>=31) return Ox80000000;

  frac|=1<<23;

  if (exp>=23){
    frac<<=(exp-23);
  }
  else frac>>=(23-exp);

  if (sign) return -frac;
  return frac;
}
